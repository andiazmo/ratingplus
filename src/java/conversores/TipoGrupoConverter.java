package conversores;

import controladores.util.JsfUtil;
import entidades.ParametriaCw;
import fachadas.ParametriaCWFacade;
import javax.ejb.EJB;
import javax.faces.bean.ManagedBean;
import javax.faces.component.UIComponent;
import javax.faces.context.FacesContext;
import javax.faces.convert.Converter;
import javax.faces.convert.FacesConverter;

/**
 *
 * @author x302266
 */
@ManagedBean
@FacesConverter(value = "tipoGrupoConverter")
public class TipoGrupoConverter implements Converter {

    @EJB
    private ParametriaCWFacade parametriaFacade;

    @Override
    public Object getAsObject(FacesContext facesContext, UIComponent component, String value) {
        if (value == null || value.length() == 0 || JsfUtil.isDummySelectItem(component, value)) {
            return null;
        }
        return this.parametriaFacade.find(getKey(value));
    }

    java.lang.String getKey(String value) {
        java.lang.String key;
        key = value;
        return key;
    }

    String getStringKey(java.lang.String value) {
        StringBuffer sb = new StringBuffer();
        sb.append(value);
        return sb.toString();
    }

    @Override
    public String getAsString(FacesContext facesContext, UIComponent component, Object object) {
        if (object == null
                || (object instanceof String && ((String) object).length() == 0)) {
            return null;
        }
        if (object instanceof ParametriaCw) {
            ParametriaCw parametria = (ParametriaCw) object;
            return getStringKey(parametria.getId().getClave());
        } else {
            return null;
        }
    }

}
